-- 2 plus 3 plus 5, benannt als sum_prime
SELECT 2 + 3 + 5 AS sum_prim;

-- 20 minus 3, benannt als sub
SELECT 20 - 3 AS sub;

-- 16 durch 2, benannt als div
SELECT 16 / 2 AS 'div';

-- 3 mal 5, benannt als mult
SELECT 3 * 5 AS mult;

-- 3 plus 5 mal 2, benannt als sum_mult
SELECT 3 + 5 * 2 AS sum_mult;